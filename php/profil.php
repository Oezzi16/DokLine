    <h2>Profile Dok_Line..</h2> <br>
        Dok_Line (Dokter Online) didirikan sejak tahun 2016, dengan Nama PT.DL Indonesia, berpusat Di Kota Bekasi.
        <br>
        Visi dan Misi didirikan nya Dok_Line yaitu : <br>
        *Visi : Melakukan terobosan baru dalam dunia kedokteran dengan Upaya memaksimalkan Teknologi yang semakin berkembang sehingga membuat segala pelayanan terhadap masyarakat menjadi semakin mudah. <br>

        *Misi : <br>
        &nbsp;&nbsp;&nbsp;&nbsp;1. Memperluas Jaringan Kedokteran yang terintegrasi dengan Aplikasi Dok_Line, bekerja sama dengan Pihak rumah sakit disekitar wilayah tertentu. <br>
        &nbsp;&nbsp;&nbsp;&nbsp;2. Meningkatkan Pelayanan selama 24 jam terhadap pasien.<br>
        &nbsp;&nbsp;&nbsp;&nbsp;3. Menjaga stabilitas kedokteran dengan meningkatkan Sertifikasi dokter yang akan bertugas.<br>
        &nbsp;&nbsp;&nbsp;&nbsp;4. Memberikan Informasi terupdate seputar kesehatan dan tips tips nya..<br>
        <br>

        Dok_Line Menargetkan Setiap tahunnya Menurunkan angka penyakit dan menjadi bagian dari pesatnya teknologi yang berkembang pada saat ini.
        Teknologi dan Kesehatan sangat akan bermanfaat jika di padukan, maka sejak saat itu Dok_Line hadir untuk melayani kesehatan anda !
